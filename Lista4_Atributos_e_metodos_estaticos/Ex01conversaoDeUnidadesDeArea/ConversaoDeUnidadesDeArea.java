public class ConversaoDeUnidadesDeArea {
    public static double metroQuadradoParaPeQuadrado(double m2) {return m2*10.76;}
    public static double peQuadradoParaCentimetroQuadrado(double p2) {return p2*929;}
    public static double milhaQuadradaParaAcre(double mi2) {return mi2*640;}
    public static double acreParaPeQuadrado(double a2) {return a2* 43560;}
    public static double peQuadradoParaAcre(double p2) { return p2/43560;}
    public static double metroQuadradoParaCmQuadrado(double m2) {return m2*1000;}
}
